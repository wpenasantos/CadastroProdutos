    private void validarCampos() throws Exception {
        if (textNome.getText() == null || textNome.getText().equals("")) {
            textNome.requestFocus();
            textNome.setBackground(new Color(255, 198, 194));
            throw new Exception(MensagensUtil.getMensagem(MensagensUtil.MSG_VALIDACAO_NOME));
        }
        if (textPreco.getText() == null || textPreco.getText().equals("")) {
            textPreco.requestFocus();
            textPreco.setBackground(new Color(255, 198, 194));
            throw new Exception(MensagensUtil.getMensagem(MensagensUtil.MSG_VALIDACAO_PRECO));
        }
    }
    
    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed
        
        try {
            
            validarCampos();
            
            if (produto != null) {

                produto.setNome(textNome.getText());
                produto.setPreco(Double.valueOf(textPreco.getText()));
                produto.getEspecificacao().setFabricante(textFabricante.getText());
                produto.getEspecificacao().setCor(textCor.getText());
                produto.getEspecificacao().setSistema(textSistema.getText());
                produto.getEspecificacao().setDetalhes(textAreaDetalhes.getText());
                
                produtoDAO.editar(produto);

            } else {

                Produto p = new Produto();
                
                p.setNome(textNome.getText());
                p.setPreco(Double.parseDouble(textPreco.getText()));
                p.getEspecificacao().setFabricante(textFabricante.getText());
                p.getEspecificacao().setCor(textCor.getText());
                p.getEspecificacao().setSistema(textSistema.getText());
                p.getEspecificacao().setDetalhes(textAreaDetalhes.getText());
                
                produtoDAO.inserir(p);
            }

            parent.preencherTabela(null);

            dispose();
           
        } catch (Exception ex) {
            //ex.printStackTrace();
            System.err.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Erro : " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonSalvarActionPerformed

